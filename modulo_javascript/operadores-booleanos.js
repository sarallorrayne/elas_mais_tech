// Operadores Booleanos: Comparações
const numero1 = 10;
const numero2 = 12;

console.log(numero1 == numero2);
console.log(numero1 === numero2);
console.log(numero1 == '10');
console.log(numero1 === '10');

// Operador de negação: !
const idade = 23;
console.log(!(idade >= 18)); // A idade NÃO é maior ou igual a 18? FALSO, é maior.